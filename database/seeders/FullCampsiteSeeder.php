    public function run(): void
    {
        $amenities = Amenity::all()->keyBy('name');
        $users     = User::all();

        if ($users->isEmpty()) {
            $this->command->warn('ユーザーが存在しないためレビューをスキップします。');
        }

        $photoIndex = 0;

        foreach ($this->sites as $i => $data) {
            $amenityNames = $data['amenities'];
            unset($data['amenities']);

            $this->command->info("[{$i}] 作成中: {$data['name']}");

            $campsite = Campsite::create(array_merge($data, ['is_active' => true]));

            // アメニティ紐付け
            $ids = collect($amenityNames)->map(fn ($n) => $amenities[$n]?->id)->filter();
            $campsite->amenities()->sync($ids);

            // 写真 (3枚)
            $typePhotos = $this->photoBank[$data['type']] ?? $this->photoBank['tent'];
            $photoSet   = $typePhotos[$photoIndex % count($typePhotos)];
            $photoIndex++;

            foreach (array_slice($photoSet, 0, 3) as $order => $photoId) {
                $this->command->line("  写真 picsum:{$photoId}...");
                $contents = $this->downloadImage("https://picsum.photos/id/{$photoId}/900/600");
                if ($contents) {
                    $path = "campsites/site_{$campsite->id}_{$order}.jpg";
                    Storage::disk('public')->put($path, $contents);
                    CampsiteImage::create(['campsite_id' => $campsite->id, 'image_path' => $path, 'sort_order' => $order]);
                }
            }

            // レビュー (ユーザーがいる場合のみ)
            if ($users->isNotEmpty()) {
                $reviewCount = rand(3, 7);
                $usedUsers   = $users->random(min($reviewCount, $users->count()));
                $label       = $this->typeLabels[$data['type']] ?? $data['type'];

                foreach ($usedUsers as $j => $user) {
                    $tpl = $this->reviewTemplates[($i + $j) % count($this->reviewTemplates)];

                    // レビューには予約が必要なので、過去の確定済み予約を作成
                    $checkIn  = now()->subDays(rand(30, 365))->startOfDay();
                    $checkOut = $checkIn->copy()->addDays(rand(1, 4));
                    $nights   = $checkIn->diffInDays($checkOut);
                    $reservation = Reservation::create([
                        'user_id'        => $user->id,
                        'campsite_id'    => $campsite->id,
                        'check_in_date'  => $checkIn,
                        'check_out_date' => $checkOut,
                        'num_guests'     => rand(1, min(4, $campsite->capacity)),
                        'total_price'    => $campsite->price_per_night * $nights,
                        'status'         => 'confirmed',
                    ]);

                    Review::create([
                        'user_id'        => $user->id,
                        'campsite_id'    => $campsite->id,
                        'reservation_id' => $reservation->id,
                        'rating'         => $tpl['rating'],
                        'comment'        => str_replace('{type}', $label, $tpl['comment']),
                    ]);
                }

                $this->command->line("  レビュー {$usedUsers->count()}件追加");
            }
        }

        $this->command->info('');
        $this->command->info('✅ 完了! '.count($this->sites).'件のキャンプサイトを作成しました。');
    }

    private function downloadImage(string $url): ?string
    {
        try {
            $res = Http::timeout(20)->get($url);
            return $res->successful() ? $res->body() : null;
        } catch (\Exception) {
            return null;
        }
    }
